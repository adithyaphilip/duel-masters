    public void startConnectActivity() {
        int cardCtr = 1;
        ArrayList<GameCard> gcs = new ArrayList<GameCard>();
        GameCard gc11 = new GameCard(cardCtr++ * 10 + 2, mDBCR.getCard(1));
        GameCard gc12 = new GameCard(cardCtr++ * 10 + 2, mDBCR.getCard(2));
        gcs.add(gc11);
        gcs.add(gc12);
        GameCard gc13 = new GameCard(cardCtr++ * 10 + 2, mDBCR.getCard(1));
        GameCard gc14 = new GameCard(cardCtr++ * 10 + 2, mDBCR.getCard(2));
        gcs.add(gc13);
        gcs.add(gc14);
        GameCard gc15 = new GameCard(cardCtr++ * 10 + 2, mDBCR.getCard(1));
        GameCard gc16 = new GameCard(cardCtr++ * 10 + 2, mDBCR.getCard(2));
        gcs.add(gc15);
        gcs.add(gc16);
        Player p1 = new Player(2, "kai", gcs);//id 2 means player is phone owner

        ArrayList<GameCard> gcs2 = new ArrayList<GameCard>();
        GameCard gc21 = new GameCard(cardCtr++ * 10 + 1, mDBCR.getCard(1));
        GameCard gc22 = new GameCard(cardCtr++ * 10 + 1, mDBCR.getCard(2));
        gcs2.add(gc22);
        gcs2.add(gc21);
        Player p2 = new Player(1, "adi", gcs2);//id 1 means player is phone opponent

        Intent i = new Intent(this, ConnectActivity.class);
        i.putExtra(ConnectActivity.KEY_OWN_CARDS, gcs);
        startActivity(i);
    }

